package CastingProg;

public class Prog1 {

	public static void main(String[] args) {

		int a = 100;
		System.out.println("A = " + a);
		double b = a;
		System.out.println("B = " + b); //100.0
		double c = 10.22;
		System.out.println("C = " + c);
		int d = (int) c;
		System.out.println("D = " + d); //10
		
	}

}
